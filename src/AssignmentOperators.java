public class AssignmentOperators {
    public static void main(String[] args) {
        int a=6;
        int b;

        b=a;
        System.out.println(b);  //6

        a+=b;
        System.out.println(a);  //12

        a-=b;
        System.out.println(a);  //10

        a*=b;
        System.out.println(a);  //36

        a/=b;
        System.out.println(a);  //6



    }
}
